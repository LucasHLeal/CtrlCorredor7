package com.bcopstein.ctrlcorredor_v7_CLEAN.negocio.entidades;

import javax.persistence.Embeddable;

@Embeddable
public class Data {
    private int dia;
    private int mes;
    private int ano;

    public Data(int dia, int mes, int ano) {
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }

    protected Data(){}

    public int getDia() {
        return dia;
    }

    public int getMes() {
        return mes;
    }

    public int getAno() {
        return ano;
    }
}
